def build_grading_prompt(question: str, model_answer: str, student_answer: str, idx: int) -> str:
    return f"""
다음은 데이터분석 수강생을 대상으로 한 과제입니다.
과제의 주제나 분야는 제한하지 않으며, 아래 정보를 바탕으로 학생의 답안을 평가해주세요.
문제 요구사항을 기준으로 학생의 답안을 평가하되, **문제에서 명시되지 않은 부분은 감점 요인으로 삼지 마세요**.
이 때, 모범 답안과 다르더라도 학생 답안이 논리적으로 타당하고 틀린 것이 없다면 상으로 평가해주세요.

📌 문제:
{question}

✅ 모범 답안:
{model_answer}

❌ 학생 답안:
{student_answer}

📝 평가 기준:

1. 학생의 답안을 '상', '중', '하' 중 하나로 평가해주세요.
    - 상: 문제를 정확히 이해하고 요구사항에 맞는 답안을 작성함
    - 중: 일부 요구사항을 충족했지만 불완전하거나 오류가 있음
    - 하: 문제를 거의 이해하지 못했거나 잘못된 접근을 함

2. 평가 이유를 간단히 2~3문장으로 설명해주세요.

결과는 아래 형식을 따르세요:

[이해도 평가] 중  
[피드백] 학생은 문제의 의도는 이해했으나, 출력 형식이 요구사항과 다릅니다.
"""
